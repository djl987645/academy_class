package src.global;
//논리 자료형
//boolean : true, false 두 가지 값만 가질 수 있는 자료형
//boolean 변수명 = true or false;
//boolean 변수명 = 조건식;
//조건식의 결과가 true이면 변수에 true가 저장되고, 조건식의 결과가 false이면 변수에 false가 저장된다.

public class Variable_07 {
    public static void main(String[] args) {
        boolean b1 = true;
        boolean b2 = false;
        boolean b3 = 10 > 5;
        boolean b4 = 10 < 5;
        System.out.println("b1 : " + b1);
        System.out.println("b2 : " + b2);  
    }
}
