package src.global;

public class Basic_05 {
    public static void main(String[] args) {
        System.out.println(10+23);
        System.out.println(10+"23");
        System.out.println("10"+23);
        System.out.println("10"+"23");

        System.out.println("10"+4+7);
        System.out.println("10"+(4+7));
        //자바는 ()안에선 좌측부터 계산
    }
}
